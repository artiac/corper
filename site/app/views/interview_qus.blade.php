                 <p class="para_big"> 
                      <h4>Sample Questions to Expect</h4>
                      <ol>
                        <li><span>Tell Me about yourself</span>..Use your “Elevator Pitch”-A 30sec summary used to simply and quickly define you.</li>
                        <li><span>What are your strengths</span>...pick 3 biggest strengths that relate to the job. Remember,  this is no time to be modest 
                          so prove your worth.</li>
                        <li><span>What are your weaknesses</span>...You should either have taken steps to redress them or turned it into a strength. </li>
                        <li><span>What are your goals and where do you see yourself in 5 years</span>….</li>
                        <li><span>What makes you better than all the other candidates</span>… state your biggest achievement and the success it brought 
                          you/previous employer end with “Given the opportunity, I could bring this success to your company.”</li>
                        <li><span>Why do you want to work here</span>… time to put the research you have done about the company to use.</li>
                        <li><span>What salary are you seeking</span>...do your research and find out how much on average people make in that position and 
                          give a mid-high range answer. This gives you more bargaining power when you are offered the Job.</li>
                        <li><span>If you were an animal which one would you be</span>… we know, its wierd but interviewers ask questions like this to 
                          evaluate your psychology or just to catch you off guard. Pick an interesting animal with good traits.</li>
                        <li><span>Problem solving questions</span>….. to see if your a quick thinker.</li>
                        <li><span>How do you deal with pressure or stressful situations</span>… talk about proven a method to reduce stress and pressure 
                          that leads to you being productive.</li>
                      </ol> 
                       <h4>Sample Questions to Ask</h4> 
                       <p>You should remember you are going to the interview to see whether you are a good fit, so you should also “interview” the business and find out more about 
                        them.</p>       
                      <ol>
                        <li> Who would supervise me?</li>
                        <li> When are you going to make a hiring decision?</li>
                        <li>What are the opportunities for advancement?</li>
                        <li>What kind of training is provided or available?</li>
                        <li>Is there a dress code?</li>
                        <li>What exactly does this company value the most, and how do you think my work for you will further these values?</li>
                        <li> What kinds of processes are in place to help me work collaboratively?</li>
                        <li>In what area could your team use a little polishing?</li>
                        <li>What’s the most important thing I can accomplish in the first 60 days?</li>
                        <li>Can you give me some examples of the most and least desirable aspects of the company’s culture</li>
                        <li>Am I going to be a mentor or will I be mentored?</li>
                        <li>How will you judge my success? What will have happened six months from now that will demonstrate that I have met your expectations?</li>
                        <li>This job sounds like something I’d really like to do -- is there a fit here?</li>
                        <li>Now that we’ve talked about my qualifications and the job, do you have any concerns about my being successful in this position?</li>
                      </ol><br>
