              <p>
                 If there is any part of the NYSC that teaches you humility and how to “kiss ass” it’s the serving phase better known as the PPA (Place of Primary Assignment). Your PPA s where you will be posted for the rest of your service year. It could be anywhere from a school to your local government office It all depends on what you got last day of Camp. The Serving phase usually involves couple things:-
              </p>
              <ol>
                <li>CD/SAED Groups:- Pick one! either develop the community (CD) or develop yourself (SAED), you do these weekly. Dont forget to get your respective cards signed.</li>
                <li>Monthly General CDS:- Every Corper in the Local Government Gathers together on this day to listen to the LGIs words of wisdom and important announcements. We would recommend coming and listening, in fact, go as far as to giving the occasional nod of agreement because your dear LGi signs your card after.</li>
                <li>Monthly Clearance:- Done in first week of the month, you get a letter from your employer saying that you worked well for the month and you deserve to get paid. Without this you don't get allowee.. dont dull.</li>
                <li>Final Clearance:- As the name says It is the Last time you do clearance. So make sure you don't vex anyone before this day. They can really make life hard for you.</li>
              </ol>
              <p>“Exactly whose ass will I be kissing?” Did we hear you ask…. the answer is simple.. anyone who has to sign anything for you , especially your boss and your LGI, they have true power. It is very advisable to get into and stay in their good books. Try not to become “teachers pet” though as you might run the risk of being hated by fellow corpers. So Kiss-ass, make friends,  take loads of pics and build your NYSC story with CorperLIfe<sup>TM</sup>!</p>
